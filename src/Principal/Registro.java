    public void Llenar() {

        u.setRowCount(0);

        for (int i = 0; i < Arrays.Registro.size(); i++) {
            u.addRow(new Object[]{
                Arrays.Registro.get(i).getNombre(),
                Arrays.Registro.get(i).getApellido(),
                Arrays.Registro.get(i).getUsuario()
            });
        }
    }

    public void Limpiar() {
        txtNombre.setText("");
        txtApellido.setText("");
        txtUsuario.setText("");
        txtPass.setText("");
    }

